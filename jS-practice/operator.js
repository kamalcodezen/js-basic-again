//  arithmetic operator 
let a = 5;
let b = 6;

console.log('a =', a, 'b =', b);
console.log("a + b =", a + b);
console.log("a -  b =", a - b);
console.log("a * b = ", a * b);
console.log("a / b =", a / b);
console.log("a ** b =", a ** b);
console.log("a % b = ", a % b);

// unary operator 
let x = 6;
let y = 3;
console.log("x = ", x, "y =", y);
// x = x + 1;
x++;
console.log("x =", x);